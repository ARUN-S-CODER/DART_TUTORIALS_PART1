void main() {
  // print statement is used to print the output on the console.
  print("Hello my dear Github Users");
  // we can use single quotes in print sentence too.
  print('You can connect me by following my github account');
  //we can pass the integer as parameter in print statement without any double quotes.
  print(2);
  /* we can use expression in the print statement.*/
  print(2 + 3);
  // when two strings operates with '+' symbol then it performs concatenation.
  print('2' + '3');
  // when string is operated with '*' symbol then it repeats the string for the give int values times.
  print('2' * 8);

  /*OUTPUT:
        Hello my dear Github Users
        You can connect me by following my github account
        2
        5
        23
        22222222
  */
}
