void main() {
  // Relational operations always return bool values(true/false)
  bool op1 = 1 > 2; // Greater than operator '>'
  bool op2 = 1 >= 2; // Greater than or equal to operator '>='
  bool op3 = 1 < 2; // Less than operator '<'
  bool op4 = 1 <= 2; // Less than or equal to operator '<='
  bool op5 = 1 == 2; // Equals to operator '=='
  bool op6 = 1 != 2; // Not Equals to operator '!='

  print(op1);
  print(op2);
  print(op3);
  print(op4);
  print(op5);
  print(op6);
  /* OUTPUT:
        false
        false
        true
        true
        false 
        true
  */
}
