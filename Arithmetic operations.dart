void main() {
  // Arithmetic operation.
  int addvalue = 4 + 5;
  // '+' used to add the values
  int subvalue = 4 - 5;
  // '-' used to subtract the values
  int mulvalue = 4 * 5;
  // '*' used to multiplication operation
  double divValue = 10 / 3;
  // '/' used to division operation
  int divValue1 = 10 ~/ 3;
  // '~/' used to floor division operation, it always return int value(similar to '//' operator in python)
  int remvalue = 10 % 3;
  // '%' used to get the remainder value while dividing two numbers.
  print(addvalue); // Output: 9
  print(subvalue); // Output: -1
  print(mulvalue); // Output: 20
  print(divValue); // Output: 3.3333333333333335
  print(divValue1); // Output: 3
  print(remvalue); // Output: 1
}
